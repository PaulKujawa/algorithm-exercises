// O(N^2)
function insertionSort(arr) {
  const array = [...arr];

  for (let i = 1; i < array.length; i++) {
    for (let j = 0; j < i; j++) {
      if (array[i] < array[j]) {
        const [item] = array.splice(i, 1);
        array.splice(j, 0, item);
      }
    }
  }

  return array;
}

module.exports = insertionSort;
